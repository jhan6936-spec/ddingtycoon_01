/**
 * Node.js 빌드 전용 — 브라우저에서는 실행되지 않습니다.
 */
const fs = require('fs');
const path = require('path');

const OAUTH_REDIRECT_DEFAULT = 'https://ddingtycoon-01.vercel.app/auth/callback';

function parseEnvFile(filePath) {
  const out = {};
  try {
    const raw = fs.readFileSync(filePath, 'utf8');
    for (const line of raw.split(/\r?\n/)) {
      const t = line.trim();
      if (!t || t.startsWith('#')) continue;
      const eq = t.indexOf('=');
      if (eq <= 0) continue;
      const key = t.slice(0, eq).trim();
      let val = t.slice(eq + 1).trim();
      if (
        (val.startsWith('"') && val.endsWith('"')) ||
        (val.startsWith("'") && val.endsWith("'"))
      ) {
        val = val.slice(1, -1);
      }
      out[key] = val;
    }
  } catch (_) {}
  return out;
}

function discordPortalRedirectFromSupabaseUrl(supabaseUrl) {
  if (!supabaseUrl || typeof supabaseUrl !== 'string') return '';
  try {
    const u = new URL(supabaseUrl.trim());
    return `${u.origin}/auth/v1/callback`;
  } catch (_) {
    return '';
  }
}

const root = path.join(__dirname, '..');
const fileEnv = parseEnvFile(path.join(root, '.env'));
const url =
  fileEnv.SUPABASE_URL ||
  (typeof process !== 'undefined' && process.env && process.env.SUPABASE_URL) ||
  '';
const anonKey =
  fileEnv.SUPABASE_ANON_KEY ||
  (typeof process !== 'undefined' && process.env && process.env.SUPABASE_ANON_KEY) ||
  '';
const oauthRedirectUrl =
  fileEnv.OAUTH_REDIRECT_URL ||
  (typeof process !== 'undefined' && process.env && process.env.OAUTH_REDIRECT_URL) ||
  OAUTH_REDIRECT_DEFAULT;
const discordOAuthRedirectUri =
  fileEnv.DISCORD_PORTAL_REDIRECT_URI ||
  (typeof process !== 'undefined' &&
    process.env &&
    process.env.DISCORD_PORTAL_REDIRECT_URI) ||
  discordPortalRedirectFromSupabaseUrl(url);

const content = `/**
 * Auto-generated by npm run build — Discord 포털용 주소는 discordOAuthRedirectUri 참고
 */
window.SUPABASE_CONFIG = ${JSON.stringify({
    url,
    anonKey,
    oauthRedirectUrl,
    discordOAuthRedirectUri
  })};
`;

const outPath = path.join(root, 'supabase-config.js');
fs.writeFileSync(outPath, content, 'utf8');
process.stdout.write(
  `[generate-env] ${path.basename(outPath)} url:${url ? 'yes' : 'empty'} anonKey:${anonKey ? 'yes' : 'empty'} oauth:${oauthRedirectUrl}\n`
);
